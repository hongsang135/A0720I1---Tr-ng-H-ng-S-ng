package B2_vong_lap_trong_java.thuc_hanh.thiet_ke_menu_cho_ung_dung;
import java.util.Scanner;
public class DesignMenuApp {
    public static void main(String[] args) {
        Scanner scanner = new Scanner(System.in);
        int choice=0;
        while(choice!= 4) {
            System.out.print("Menu\n1.Draw the triangle\n2.Draw the square\n3.Draw the rectangle\n4.Exit\nEnter your choice:");
            choice = scanner.nextInt();
            switch (choice) {
                case 1:
                    System.out.println("Draw the triangle");
                    System.out.println("******");
                    System.out.println("*****");
                    System.out.println("****");
                    System.out.println("***");
                    System.out.println("**");
                    System.out.println("*");
                    break;
                case 2:
                    System.out.println("Draw the square");
                    System.out.println("* * * * * *");
                    System.out.println("* * * * * *");
                    System.out.println("* * * * * *");
                    System.out.println("* * * * * *");
                    System.out.println("* * * * * *");
                    System.out.println("* * * * * *");
                    break;
                case 3:
                    System.out.println("Draw the rectangle");
                    System.out.println("* * * * * *");
                    System.out.println("* * * * * *");
                    System.out.println("* * * * * *");
                    break;
                case 0:
                    System.exit(0);
                default:
                    System.out.println("No choice!");
            }
        }
    }
}
